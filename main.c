/**
 * Period of match when driver controls the bot
 */
task usercontrol(){
	startTask(userControlls);
	bool isIntakeOn = false;
	bool isLauncherOn = false;
	bool isIntakeButton = false;
	bool isLauncherButton = false;
  while (true)
	{
    if(bVEXNETActive){
				/////////////////////////////////////////////////////////////////////////////////////////
      //
      //                                      Drive
      //
      /////////////////////////////////////////////////////////////////////////////////////////
  		motor[RD] = vexRT[Ch3];
  		motor[LD] = vexRT[Ch2];


			if(vexRT[Btn6D] == 1){
				if(isLauncherButton == false){
					if(isLauncherOn){
						motor[LL] = 0;
						motor[LR] = 0;
						isLauncherOn = false;
					}else{
						motor[LL] = 90;
						motor[LR] = 90;
						isLauncherOn = true;
					}
					isLauncherButton = true;
				}
			}
			if(vexRT[Btn5D] == 1){
				if(isIntakeButton == false){
					if(isIntakeOn){
						motor[intake] = 0;
						isIntakeOn = false;
					}else{
						motor[intake] = 90;
						isIntakeOn = true;
					}
					isIntakeButton = true;
				}
			}

			if(vexRT[Btn6D] == 0){
				isLauncherButton = false;
			}

			if(vexRT[Btn5D] == 0){
				isIntakeButton = false;

			}

      wait1Msec(10);
    }
	}
}
